package org.ait.msc.project;

public class StudentReport {
	
	String studentName;
	String parentName;
	String overallProgress;
	String additionProficiency;
	String subtractionProficiency;
	String multipicationProficiency;
	String divisionProficiency;
	String strongTables;
	String weakTables;
	
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public String getParentName() {
		return parentName;
	}
	public void setParentName(String parentName) {
		this.parentName = parentName;
	}
	public String getOverallProgress() {
		return overallProgress;
	}
	public void setOverallProgress(String overallProgress) {
		this.overallProgress = overallProgress;
	}
	public String getAdditionProficiency() {
		return additionProficiency;
	}
	public void setAdditionProficiency(String additionProficiency) {
		this.additionProficiency = additionProficiency;
	}
	public String getSubtractionProficiency() {
		return subtractionProficiency;
	}
	public void setSubtractionProficiency(String subtractionProficiency) {
		this.subtractionProficiency = subtractionProficiency;
	}
	public String getMultipicationProficiency() {
		return multipicationProficiency;
	}
	public void setMultipicationProficiency(String multipicationProficiency) {
		this.multipicationProficiency = multipicationProficiency;
	}
	public String getDivisionProficiency() {
		return divisionProficiency;
	}
	public void setDivisionProficiency(String divisionProficiency) {
		this.divisionProficiency = divisionProficiency;
	}
	public String getStrongTables() {
		return strongTables;
	}
	public void setStrongTables(String strongTables) {
		this.strongTables = strongTables;
	}
	public String getWeakTables() {
		return weakTables;
	}
	public void setWeakTables(String weakTables) {
		this.weakTables = weakTables;
	}
	
}
